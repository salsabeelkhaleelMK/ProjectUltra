/**
 * Translate a text string based on locale
 * @param {string} text - Text to translate
 * @param {string} locale - Target locale (it, de, fr, nl)
 * @returns {string} Translated text or original if no translation found
 */
export function translateText(text, locale) {
  if (!text || !locale || locale === 'en') {
    return text
  }
  
  const localeTranslations = translations[locale]
  if (!localeTranslations) {
    return text
  }
  
  // Exact match
  if (localeTranslations[text]) {
    return localeTranslations[text]
  }
  
  // Partial match for common patterns
  for (const [key, value] of Object.entries(localeTranslations)) {
    if (text.includes(key)) {
      return text.replace(key, value)
    }
  }
  
  return text
}

/**
 * Translate an object's text fields recursively
 * @param {Object} obj - Object to translate
 * @param {string} locale - Target locale
 * @param {Array<string>} fields - Fields to translate (default: common fields)
 * @returns {Object} Translated object
 */
export function translateObject(obj, locale, fields = ['requestMessage', 'notes', 'content', 'summary', 'action']) {
  if (!obj || !locale || locale === 'en') {
    return obj
  }
  
  if (Array.isArray(obj)) {
    return obj.map(item => translateObject(item, locale, fields))
  }
  
  if (typeof obj !== 'object') {
    return obj
  }
  
  const translated = { ...obj }
  
  for (const [key, value] of Object.entries(translated)) {
    if (fields.includes(key) && typeof value === 'string') {
      translated[key] = translateText(value, locale)
    } else if (typeof value === 'object' && value !== null) {
      translated[key] = translateObject(value, locale, fields)
    }
  }
  
  return translated
}
